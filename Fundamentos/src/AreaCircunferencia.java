public class AreaCircunferencia {
    public static void main(String[] args) {
        // Declara as variáveis e constantes necessárias para a execução do programa
        double raio = 3.4;
        final double PI = 3.14159;
        // Realiza o cálculo da á area da circunferência e imprime na saída do console
        double area = PI * raio * raio;
        System.out.println(area);
    }
}
